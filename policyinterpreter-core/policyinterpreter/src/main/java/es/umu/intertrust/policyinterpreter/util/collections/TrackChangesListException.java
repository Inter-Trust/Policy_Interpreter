package es.umu.intertrust.policyinterpreter.util.collections;

/**
 *
 * @author Juanma
 */
public class TrackChangesListException extends Exception {

    /**
     * Creates a new instance of <code>TrackChangesListException</code> without
     * detail message.
     */
    public TrackChangesListException() {
    }

    /**
     * Constructs an instance of <code>TrackChangesListException</code> with the
     * specified detail message.
     *
     * @param msg the detail message.
     */
    public TrackChangesListException(String msg) {
        super(msg);
    }
}
